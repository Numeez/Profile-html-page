public class Innings {
    private String team;
    private String inning;
    private int runs;
    public void setTeam(String team) {
        this.team = team;
    }
    public String getTeam() {
        return team;
    }
    public void setRuns(int runs) {
        this.runs = runs;
    }
    public int getRuns() {
        return runs;
    }

    public void setInning(String inning) {
        this.inning = inning;
    }
    public String getInning() {
        return inning;
    }
}
